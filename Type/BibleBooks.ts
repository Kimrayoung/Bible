export function convertFullNameToAbbreviation(bookName: string): string {
  // 전체 이름이 매핑에 있는지 확인
  if (bookName in fullNameToAbbrev) {
    return fullNameToAbbrev[bookName as BibleBookFullName];
  }

  // 매핑에 없는 경우 원래 이름 반환
  return bookName;
}
